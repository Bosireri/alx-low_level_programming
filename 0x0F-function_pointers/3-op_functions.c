#include "3-calc.h"

/**
 * op_add - entry point
 *
 * @a: arg 1 (integer variable)
 * @b: arg 2 (integer variable)
 *
 * Return: result of operation
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - entry point
 *
 * @a: arg 1 (integer variable)
 * @b: arg 2 (integer variable)
 *
 * Return: result of operation
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - entry point
 *
 * @a: arg 1 (integer variable)
 * @b: arg 2 (integer variable)
 *
 * Return: result of operation
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - entry point
 *
 * @a: arg 1 (integer variable)
 * @b: arg 2 (integer variable)
 *
 * Return: result of operation
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - entry point
 *
 * @a: arg 1 (integer variable)
 * @b: arg 2 (integer variable)
 *
 * Return: result of operation
 */
int op_mod(int a, int b)
{
	return (a % b);
}
